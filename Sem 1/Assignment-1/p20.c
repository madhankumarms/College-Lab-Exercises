// Size of property

#include<stdio.h>
void main()
{
	int i = 4;
	long l = 5;
	float f = 6;
	double d = 7;
	char c = 'a';
	
	printf("The size of a integer is %d\n",sizeof(i));
	
	printf("The size of a long is %d\n",sizeof(l));
	
	printf("The size of a float is %d\n",sizeof(f));
	
	printf("The size of a double is %d\n",sizeof(d));
	
	printf("The size of a character is %d\n",sizeof(c));
	
}
